				<div style="font-size:14px; font-weight:bold; color: #ff3300"> The Blinds <br>					
				<p> Dalam permainan Hold'em, player yang mendapatkan button atau tanda "D" biasa disebut "the Dealer Button". Sebelum kartu dibagikan, player pertama yang duduk searah jarum jam disamping player yang mendapatkan button D secara otomatis akan meletakkan chips diatas meja sejumlah "Small Blind", selanjutnya player disamping Small Blind ini yang searah jarum jam secara otomatis akan meletakkan chips sejumlah "Big Blind" dimana jumlah "Big Blind" ini adalah 2x lebih besar dari jumlah "Small Blind". <br> <br>

				Sebelum player menentukan pilihan meja untuk bermain, player dapat menentukan jumlah ketentuan limit "Small Blind" dan "Big Blind" yang mereka inginkan, ini dapat dilihat dari angka misalnya 300/600, 500/1000, 1000/2000 atau yang lainnya. <br> <br>

				Setelah semua proses diatas selesai, kini dealer akan membagikan 2 hole cards ke masing-masing pemain. Selanjutnya proses betting akan dimulai searah dengan jarum jam, dimulai dari pemain yang duduk disamping "Big Blind".</p> <br>
				</div>
				
				<div style="font-size:14px; font-weight:bold; color: #ff3300"> Pre-Flop <br>					
				<p> Setelah semua pemain masing-masing telah mendapatkan 2 kartu, kini setiap pemain mendapatkan opsi untuk memainkan kartu mereka dengan pilihan "call", "raise", atau "fold".</p> <br>
				</div>
				
				<div style="font-size:14px; font-weight:bold; color: #ff3300"> The Flop <br>					
				<p> Setelah sesi Pre-Flop selesai, kini dealer akan membagikan 3 kartu diatas meja dengan posisi terbuka, ini disebut dengan The Flop. Pada putaran ini, opsi pilihan betting selanjutnya jatuh ke pemain yang duduk disamping "dealer button" searah jarum jam. Opsi yang bias dipilih adalah sama seperti opsi pada Pre-Flop, namun ada satu tambahan opsi Check jika pemain tidak ingin melakukan betting dan selanjutnya akan berlanjut ke pemain berikutnya.</p> <br>
				</div>
				
				<div style="font-size:14px; font-weight:bold; color: #ff3300"> The Turn <br>					
				<p> Ketika sesi betting pada The Flop telah selesai, dealer akan membagikan kartu keempat diatas meja, sesi ini disebut dengan The Turn. Putaran taruhan berlanjut sama seperti sesi The Flop.</p> <br>
				</div>
				
				<div style="font-size:14px; font-weight:bold; color: #ff3300"> The River <br>					
				<p> Ketika sesi taruhan pada sesi The Turn selesai, maka selanjutnya dealer akan membagikan kartu terkahir yaitu kartu kelima diatas meja, sesi ini disebut dengan sesi terakhir atau The River. Opsi pilihan betting sama seperti yang disebutkan diatas.</p> <br>
				</div>
				
				<div style="font-size:14px; font-weight:bold; color: #ff3300"> The Showdown <br>					
				<p> Jika terdapat lebih dari satu pemain yang tersisa pada sesi The River, maka pemain terakhir yang melakukan pemasangan taruhan adalah pemain yang akan membuka kartu duluan, kecuali tidak ada yang melakukan pemasangan taruhan maka player yang berada disamping "dealer button" lah yang pertama akan membuka kartu. Pemenang ditentukan berdasarkan pemegang kombinasi kartu terbaik.</p> <br>
				</div>
