				<div> <font style="font-size:14px; font-weight:bold; color: #ff3300"> 1. Check </font> - Jika player sebelumnya tidak ada melakukan pemasangan taruhan, maka pemain selanjutnya dapat melakukan opsi pilihan Check, jika semua pemain melakukan Check maka sesi putaran taruhan tersebut dianggap selesai dan sesi baru putaran selanjutnya dimulai.					
				<br><br></div>
				
				<div> <font style="font-size:14px; font-weight:bold; color: #ff3300"> 2. Bet </font> - Jika tidak ada taruhan yang dilakukan sebelumnya maka pemain dapat memilih opsi pilihan Bet. Jika pemain melakukan Bet, maka pemain selanjutnya dapat memilih opsi antara Fold, Call atau Raise.					
				<br><br></div>
				
				<div> <font style="font-size:14px; font-weight:bold; color: #ff3300"> 3. Fold </font> - Opsi pilihan Fold adalah pilihan yang dilakukan oleh seorang pemain jika pemain tersebut menyerah. Pemain tersebut tidak dapat melakukan pemasangan taruhan lagi dan dianggap gugur.					
				<br><br></div>
				
				<div> <font style="font-size:14px; font-weight:bold; color: #ff3300"> 4. Call </font> - Jika terdapat pemasangan taruhan pada pemain sebelumnya, pemain selanjutnya dapat mengikuti besaran nilai taruhan tersebut dengan memilih pilihan Call.					
				<br><br></div>
				
				<div> <font style="font-size:14px; font-weight:bold; color: #ff3300"> 5. Raise </font> - Jika terdapat pemasangan taruhan pada pemain sebelumnya, pemain berikutnya dapat melakukan penambahan dari nilai taruhan sebelumnya dengan melakukan pilihan Raise, atau jika pemain tersebut ingin menghabiskan semua chips yang dimilikinya diatas meja maka pemain tersebut dapat melakukan pilihan All-In.					
