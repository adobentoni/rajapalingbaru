				<div> Urutan Ranking Kartu Texas Hold'em dimulai dari yang tertinggi adalah sebagai berikut : <br> <br> </div>
				<div style="font-size:14px; font-weight:bold; color: #ff3300"> 1. &nbsp; Royal Flush <br>
				<img src="images/royalflush (2).png">		
				<p> Urutan yang paling tinggi dan tidak terkalahkan. 5 kartu ini harus terdiri dari Ace, King, Queen, Jack, 10 dan harus memiliki corak yang sama.</p><br>
				</div>
								
				<div style="font-size:14px; font-weight:bold; color: #ff3300"> 2. &nbsp; Straight  Flush <br>
				<img src="images/straightflush (2).png">		
				<p> 5 kartu yang berurutan dan memiliki corak yang sama. Jika ada player lain yang memiliki Straight Flush juga, maka pemenang ditentukan dari kartu yang memiliki nilai tertinggi dari urutan kartu tersebut. <br> Contoh : Kartu 7,8,9,10,J menang terhadap kartu 6,7,8,9,10</p><br>
				</div>
				
				<div style="font-size:14px; font-weight:bold; color: #ff3300"> 3. &nbsp; Four of a Kind <br>
				<img src="images/fourofakind (2).png">		
				<p> 4 kartu kembar dan 1 kartu samping sebagai "kicker". Jika player lain juga memiliki kartu Four of a Kind, maka player yang memilki kartu Four of a Kind tertinggi yang menang. Jika kartu Four of a Kind berada diatas meja (community cards), maka player yang memiliki kartu kelima ("kicker") tertinggi yang menang.</p><br>
				</div>
				
				<div style="font-size:14px; font-weight:bold; color: #ff3300"> 4. &nbsp; Full House <br>
				<img src="images/fullhouse (2).png">		
				<p> 3 kartu kembar, dan 2 kartu kembar lainnya. Jika player lain juga memiliki Full House, maka player dengan 3 kartu kembar tertinggi yang menang. Jika 3 kartu kembar berada diatas meja (community cards), maka player dengan 2 kartu kembar tertinggi yang menang.</p><br>
				</div>
								
				<div style="font-size:14px; font-weight:bold; color: #ff3300"> 5. &nbsp; Flush <br>
				<img src="images/flush (2).png">		
				<p> 5 kartu dengan corak yang sama. Jika player lain juga memiliki kartu Flush, maka player yang memegang nilai kartu tertinggi yang menang, jika masih seimbang maka kartu kedua-tertinggi, ketiga-tertinggi, keempat-tertinggi dan kelima-tertinggi akan digunakan untuk mencari pemenangnya. Jika sampai kartu kelima-tertinggi juga masih sama, maka semua nilai taruhan yang berada diatas meja akan dibagi rata ke para pemenang. Corak kartu tidak dapat digunakan untuk penentuan kemenangan.</p><br>
				</div>
				
				<div style="font-size:14px; font-weight:bold; color: #ff3300"> 6. &nbsp; Straight <br>
				<img src="images/straight (2).png">		
				<p> 5 kartu yang memiliki nilai berurutan. Jika player lain juga memiliki kartu Straight, maka pemenang ditentukan dengan kartu yang memiliki nilai tertinggi. Kartu Ace dapat diposisikan pada urutan tertinggi maupun terendah. A,K,Q,J,10 (maka Ace adalah tertinggi), 5,4,3,2,A (maka Ace adalah terendah, 5 tertinggi).</p><br>
				</div>
				
				<div style="font-size:14px; font-weight:bold; color: #ff3300"> 7. &nbsp; Three of a Kind <br>
				<img src="images/Threeofakind (2).png">		
				<p> 3 kartu kembar dan sisa 2 kartu samping acak lainnya. Jika player lain juga memiliki kartu Three of a Kind, maka player dengan kartu Three of a Kind tertinggi yang menang. Jika kartu Three of a Kind berada diatas meja (community cards), maka player yang memiliki kartu samping dengan nilai tertinggi yang menang, jika masih seimbang maka kartu kedua-tertinggi yang akan menang.</p><br>
				</div>
				
				<div style="font-size:14px; font-weight:bold; color: #ff3300"> 8. &nbsp; Two Pair <br>
				<img src="images/twopair (2).png">		
				<p> 2 pasang kartu kembar yang berbeda dan 1 kartu samping acak lainnya. Jika beberapa players memiliki kartu Two Pair, maka pemegang kartu kembar dengan nilai tertinggi yang menang, jika masih seimbang maka kartu kembar kedua-tertinggi yang menang, dan jika masih seimbang juga maka pemegang kartu samping dengan nilai tertinggi yang menang.</p><br>
				</div>
								
				<div style="font-size:14px; font-weight:bold; color: #ff3300"> 9. &nbsp; One Pair <br>
				<img src="images/onepair (2).png">		
				<p> 2 kartu kembar dan 3 kartu acak lainnya. Dalam keadaan seimbang, maka player dengan nilai kartu acak tertinggi yang menang, jika masih seimbang juga maka kartu acak kedua-tertinggi atau ketiga-tertinggi akan digunakan untuk penentuan kemenangan.</p><br>
				</div>
				
				<div style="font-size:14px; font-weight:bold; color: #ff3300"> 10. &nbsp; High Card <br>
				<img src="images/highcard (2).png">		
				<p> Susunan kartu acak yang sama sekali tidak memenuhi kriteria jenis kartu diatas sebelumnya. Penentuan kemenangan akan diambil berdasarkan nilai kartu tertinggi atau kedua-tertinggi, ketiga-tertinggi, keempat-tertinggi dan kelima-tertinggi.</p>
